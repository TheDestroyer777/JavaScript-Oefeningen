const convertCelciusToFahrenheit = celsius => {
    return celsius * (9/5) + 32;
};

console.log(convertCelciusToFahrenheit(30));
console.log(convertCelciusToFahrenheit(0));
console.log(convertCelciusToFahrenheit(20));
console.log(convertCelciusToFahrenheit(15));
console.log(convertCelciusToFahrenheit(18));